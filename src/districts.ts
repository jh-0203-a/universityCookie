// 배지에 보여줄 짧은 지역명을 만듭니다.
//  - '경기도 수원시' → '수원',  '서울특별시 강남구' → '강남',  '인천광역시 부평구' → '부평'
//  - 줄였을 때 한 글자만 남으면(예: '중구' → '중') 어색하니 원래 이름을 그대로 둡니다.
export function shortDistrict(regionCity?: string): string {
  if (!regionCity) return '';
  const last = regionCity.trim().split(/\s+/).pop() ?? ''; // 마지막 토큰(시군구)만
  const stripped = last.replace(/(특별시|광역시|특별자치시|특별자치도|[시군구도])$/u, '');
  return stripped.length >= 2 ? stripped : last;
}
